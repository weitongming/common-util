    @SneakyThrows
    public void testIov(String dateBegin,String dateEnd,List<String> vinList ,String fileName){


        List<IOVStatistic> res = carNetWorkingFunctionMapper.selectByDateAndVin(dateBegin,dateEnd,vinList);
        List<String> headFirst = new ArrayList<>();
        headFirst.add("日期");
        headFirst.add("车架号");
        List<String> tail = res.stream().map(IOVStatistic::getBehavior).distinct().collect(Collectors.toList());
        headFirst.addAll(tail);
        //写头
        Workbook workbook = new SXSSFWorkbook();
        Sheet sheet =  ExcelWriter.buildDataSheet(workbook,headFirst);
        Map<String,List<IOVStatistic>> map = res.stream().collect(Collectors.groupingBy(IOVStatistic::getVin));
        List<List<String>> allRow = map.entrySet().stream().map(k->{
            Map<String,Integer> tagMap = k.getValue().stream().collect(Collectors.toMap(IOVStatistic::getBehavior , IOVStatistic::getCount));

            List<String> rowData = new ArrayList<>();
            //加vin
            rowData.add(k.getValue().get(0).getDay());
            rowData.add(k.getKey());
            tail.forEach(head ->{
                rowData.add(String.valueOf(tagMap.getOrDefault(head,0)));
            });
            return rowData;
        }).collect(Collectors.toList());
        ExcelWriter.writeRow(allRow,sheet);
        OutputStream out = new FileOutputStream(fileName+".xlsx");
        workbook.write(out);


    }


